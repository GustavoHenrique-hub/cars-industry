public class Engine {
    Integer numCylinder;
    Integer power;

    public Integer getNumCylinder() {

        return this.numCylinder;
    }

    public void setNumCylinder(Integer numCylinder) {

        this.numCylinder = numCylinder;
    }

    public Integer getPower() {
        return this.power;
    }

    public void setPower(Integer power) {

        this.power = power;
    }
}
